import type { KeyCodeEntry, ModifierFlags } from './types';

export function computeModifierBitmask(flags: ModifierFlags): number {
  let mask = 0;
  if (flags.shift) mask += 2;
  if (flags.option) mask += 4;
  if (flags.cmd) mask += 8;
  if (flags.ctrl) mask += 4096;
  return mask;
}

